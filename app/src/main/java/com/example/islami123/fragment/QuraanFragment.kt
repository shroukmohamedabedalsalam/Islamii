        recycler_view.adapter = surasAdapter
        surasAdapter.onItemClickListener = object: SurasAdapter.OnItemClickListener{
            override fun onItemClick(position: Int, name: String) {
                startSuraDetails(position,name)
            }
        }
    }

    private fun startSuraDetails(position: Int, name: String) {
        val fileName = ""+(position+1)+".txt"
        val intent = Intent(context,SuraDetailsActivity::class.java)
        intent.putExtra(Constance.EXTRA_SURA_NAME,name)
        intent.putExtra(Constance.EXTRA_FILE_NAME,fileName)

        startActivity(intent)


    }

